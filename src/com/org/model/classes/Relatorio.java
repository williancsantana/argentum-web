package com.org.model.classes;


/**
 *
 * @author geraldo
 */
public class Relatorio {
    private String nome;
    private String nomeArquivoSelecaoDbf;
    private String txtAviso;
    private String txtLabelData;
    private String txtLabelUF;
    private String txtLabelMunicipio;
    private String txtLabelRegional;
    private String pathJasperFile;
    private String agravo;


    public Relatorio(String nome, String nomeArquivo, String aviso,String lblData,String lblUF, String lblMunicipio,String lblRegional,String jasper,String agravo){
        this.nome=nome;
        this.nomeArquivoSelecaoDbf=nomeArquivo;
        this.agravo=agravo;
        this.pathJasperFile=jasper;
        this.txtAviso=aviso;
        this.txtLabelData=lblData;
        this.txtLabelMunicipio = lblMunicipio;
        this.txtLabelRegional = lblRegional;
        this.txtLabelUF=lblUF;
    }

    public Relatorio(String nome,String agravo){
        this.nome = nome;
        this.agravo = agravo;
    }



    public String getNome() {
        return nome;
    }

    public void setNome(String nome) {
        this.nome = nome;
    }

    public String getNomeArquivoSelecaoDbf() {
        return nomeArquivoSelecaoDbf;
    }

    public void setNomeArquivoSelecaoDbf(String nomeArquivoSelecaoDbf) {
        this.nomeArquivoSelecaoDbf = nomeArquivoSelecaoDbf;
    }

    public String getTxtAviso() {
        return txtAviso;
    }

    public void setTxtAviso(String txtAviso) {
        this.txtAviso = txtAviso;
    }

    public String getTxtLabelData() {
        return txtLabelData;
    }

    public void setTxtLabelData(String txtLabelData) {
        this.txtLabelData = txtLabelData;
    }

    public String getTxtLabelUF() {
        return txtLabelUF;
    }

    public void setTxtLabelUF(String txtLabelUF) {
        this.txtLabelUF = txtLabelUF;
    }

    public String getTxtLabelMunicipio() {
        return txtLabelMunicipio;
    }

    public void setTxtLabelMunicipio(String txtLabelMunicipio) {
        this.txtLabelMunicipio = txtLabelMunicipio;
    }

    public String getTxtLabelRegional() {
        return txtLabelRegional;
    }

    public void setTxtLabelRegional(String txtLabelRegional) {
        this.txtLabelRegional = txtLabelRegional;
    }

    public String getPathJasperFile() {
        return pathJasperFile;
    }

    public void setPathJasperFile(String pathJasperFile) {
        this.pathJasperFile = pathJasperFile;
    }

    /**
     * @return the agravo
     */

    public String getagravo() {
        return agravo;

    }
    /**
     * @param agravo the agravo to set
     */

    public void setagravo(String agravo) {
        this.agravo = agravo;
    }

   


}
