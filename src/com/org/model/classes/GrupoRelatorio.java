package com.org.model.classes;

import java.util.ArrayList;

/**
 *
 * @author geraldo
 */
public class GrupoRelatorio {
    private ArrayList<Relatorio> relatorios;
    private String nome;
    private boolean hasAgravos;

    public GrupoRelatorio(String nome){
        this.nome = nome;
    }
    /**
     * @return the relatorios
     */
    public ArrayList<Relatorio> getRelatorios() {
        return relatorios;
    }

    /**
     * @param relatorios the relatorios to set
     */
    public void setRelatorios(ArrayList<Relatorio> relatorios) {
        this.relatorios = relatorios;
    }

    /**
     * @return the nome
     */
    public String getNome() {
        return nome;
    }

    /**
     * @param nome the nome to set
     */
    public void setNome(String nome) {
        this.nome = nome;
    }


    /**
     * @return the hasAgravos
     */
    public boolean isHasAgravos() {
        return hasAgravos;
    }

    /**
     * @param hasAgravos the hasAgravos to set
     */
    public void setHasAgravos(boolean hasAgravos) {
        this.hasAgravos = hasAgravos;
    } 


}
