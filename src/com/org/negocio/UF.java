package com.org.negocio;

/**
 *
 * @author geraldo
 */
public class UF {
    private int cdUF;
    private String nmUF;
    private Regiao regiao;
    private int qtdMunicipios;
    private int qtdMunicipiosSemNotificacao;
    

    /**
     * @return the cdUF
     */
    public int getCdUF() {
        return cdUF;
    }

    /**
     * @param cdUF the cdUF to set
     */
    public void setCdUF(int cdUF) {
        this.cdUF = cdUF;
    }

    /**
     * @return the nmUF
     */
    public String getNmUF() {
        return nmUF;
    }

    /**
     * @param nmUF the nmUF to set
     */
    public void setNmUF(String nmUF) {
        this.nmUF = nmUF;
    }

    /**
     * @return the regiao
     */
    public Regiao getRegiao() {
        return regiao;
    }

    /**
     * @param regiao the regiao to set
     */
    public void setRegiao(Regiao regiao) {
        this.regiao = regiao;
    }

    /**
     * @return the qtdMunicipios
     */
    public int getQtdMunicipios() {
        return qtdMunicipios;
    }

    /**
     * @param qtdMunicipios the qtdMunicipios to set
     */
    public void setQtdMunicipios(int qtdMunicipios) {
        this.qtdMunicipios = qtdMunicipios;
    }

    /**
     * @return the qtdMunicipiosSemNotificacao
     */
    public int getQtdMunicipiosSemNotificacao() {
        return qtdMunicipiosSemNotificacao;
    }

    /**
     * @param qtdMunicipiosSemNotificacao the qtdMunicipiosSemNotificacao to set
     */
    public void setQtdMunicipiosSemNotificacao(int qtdMunicipiosSemNotificacao) {
        this.qtdMunicipiosSemNotificacao = qtdMunicipiosSemNotificacao;
    }

}
